class Product {
  String productName;
  String price;
  String rating;
  String category;
  String timeTaken;
  String distance;
  String productImages;
  String sellerName;
  Product({
    required this.category,
    required this.distance,
    required this.price,
    required this.productImages,
    required this.productName,
    required this.rating,
    required this.timeTaken,
    required this.sellerName,
  });
}
